function importAll(r) {
  return r
    .keys()
    .sort((f, s) => {
      const fNum = parseInt(f.match(/\d+/g).join(''), 10);
      const sNum = parseInt(s.match(/\d+/g).join(''), 10);
      return fNum - sNum;
    })
    .map(r)
    .map(el => el.default);
}
